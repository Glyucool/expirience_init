// Функція для підняття числа x до ступеня y
function pow(x, y) {
    // Змінна для збереження результату
    let result = 1;

    // Цикл, який перемножує x на себе y разів
    for (let i = 0; i < y; i++) {
        result *= x;
    }

    // Повертаємо результат
    return result;
}

// Приклад використання функції
console.log(pow(2, 3)); // Повинно вивести 8